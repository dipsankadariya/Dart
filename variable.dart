void main() {
  //variables in dart

  //use int datatype to store integer value
  int a = 10;
  print(a);

  //String
  String name = "dipsan";
  print(name);

  //double
  double value = 10.5;
  print(value);

  //dynamic
  dynamic caste = 'kadariya';
  print(caste);

  //consts
  const String fullName = 'dipsan kadariya';
  //finals
  final String nickName = 'king';

  print(fullName);
  print(nickName);
}
