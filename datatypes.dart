void main() {
  // Data Types
  // Numbers, Strings, Booleans, Lists, Maps, Dynamic

  // Strings
  String firstName = "Dipsan";
  print("String: $firstName");

  // Integers
  int myNum = 20;
  print("Integer: $myNum");

  // Doubles
  double num = 19.95;
  print("Double: $num");

  // Booleans
  bool myBool = true;
  print("Boolean: $myBool");

  // Dynamic
  dynamic fullName = "Dipsan kadariya";
  print('Dynamic: $fullName');
}
